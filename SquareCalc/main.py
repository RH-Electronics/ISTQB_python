# This is a simple class usage example.

from square import Square

sq1 = Square(3,4)
sq2 = Square(2,5)


print(f"Area of the first square is {sq1.CalcArea()}")
print(f"Perimeter of the first square is {sq1.CalcHekef()}")

print(f"Area of the second square is {sq2.CalcArea()}")
print(f"Perimeter of the second square is {sq2.CalcHekef()}")
